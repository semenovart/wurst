import { describe, it, expect } from "vitest";
import { isProfane } from "./moderation";

describe("модерация: ловит", () => {
  const bad = [
    "хуй",
    "ХУЙ",
    "х у й",
    "х.у.й",
    "xyй", // латинские двойники
    "пизда",
    "пи3да", // leet
    "бляяядь",
    "ёбаный",
    "заебало",
    "пидорас",
    "гандон",
    "fuck you",
    "fuuuck",
    "bitch",
    "сука",
    "жопа",
    "Счастья, здоровья, хуй вам",
  ];
  bad.forEach((s) => {
    it(`блокирует «${s}»`, () => {
      expect(isProfane(s)).toBe(true);
    });
  });
});

describe("модерация: пропускает", () => {
  const good = [
    "Совет да любовь!",
    "Пусть будет солнце",
    "оскорблять никого не будем",
    "употреблять только счастье",
    "истребитель туч",
    "сто рублей на счастье",
    "команда мечты",
    "мандарины на столе",
    "скипидарные ванны",
    "Люблю вас, Маша и Дима",
    "герб семьи",
    "Прекрасного неба!",
    "шлём лучи добра", // «шлём» ≠ «шлюх»
    "Артём",
    "Ксения",
  ];
  good.forEach((s) => {
    it(`пропускает «${s}»`, () => {
      expect(isProfane(s)).toBe(false);
    });
  });
});
